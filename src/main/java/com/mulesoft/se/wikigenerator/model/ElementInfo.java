package com.mulesoft.se.wikigenerator.model;

public class ElementInfo {

	private String complexTypeXml;
	
	private String name;
	
	private String documentation;

	public String getComplexTypeXml() {
		return complexTypeXml;
	}

	public void setComplexTypeXml(String complexTypeXml) {
		this.complexTypeXml = complexTypeXml;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDocumentation() {
		return documentation;
	}

	public void setDocumentation(String documentation) {
		this.documentation = documentation;
	}
	
	
}
